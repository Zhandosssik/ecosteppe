export type Locale = "ru" | "kk";

const fmtRu = (n: number) => new Intl.NumberFormat("ru-RU").format(n);
const fmtKk = (n: number) => new Intl.NumberFormat("kk-KZ").format(n);

function daysLabelRu(days: number): string {
  const mod10 = days % 10;
  const mod100 = days % 100;
  if (mod10 === 1 && mod100 !== 11) return "день";
  if (mod10 >= 2 && mod10 <= 4 && (mod100 < 10 || mod100 >= 20)) return "дня";
  return "дней";
}

function daysLabelKk(_days: number): string {
  return "күн";
}

